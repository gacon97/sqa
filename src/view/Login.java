    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        User user = new User(txtEmail.getText(), txtPassword.getText());
        LoginController loginController = new LoginController();
        if(user.getEmail().equalsIgnoreCase("nam@gmail.com") && user.getPassword().equalsIgnoreCase("secret"))
        {
            RegisterCustomer registerCustomer = new RegisterCustomer();
            registerCustomer.setVisible(true);
            this.setVisible(false);
        }
        else if(loginController.checkLogin(user))
        {
            InformationLoan informationLoan = new InformationLoan();
            informationLoan.setVisible(true);
            this.setVisible(false);
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Email hoặc mật khẩu không đúng");
        }
        
    }//GEN-LAST:event_btnLoginActionPerformed

